n, m, v = map(int, input().split())

graph = [[] for i in range(n)]

for i in range(m):
  node, next = map(int, input().split())
  graph[node].append(next)

def dfs(graph, v, visited):
  visited[v] = True
  print(v, end='')
  for j in graph[v]:
    if not visited[j]:
      dfs(graph, i, visited)

visited = [False] * (n+1)

dfs(graph, v, visited)